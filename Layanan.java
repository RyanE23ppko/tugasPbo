public class Layanan {
    public class Layanan {
        private String deskripsi;
        private double biaya;

        public Layanan(String deskripsi, double biaya) {
            this.deskripsi = deskripsi;
            this.biaya = biaya;
        }

        public String getDeskripsi() {
            return deskripsi;
        }

        public double getBiaya() {
            return biaya;
        }
    }

}
